CREATE TABLE CUSTOMER (
CONTACT_ID SERIAL NOT NULL,
FIRST_NAME VARCHAR(500),
LAST_NAME VARCHAR(500),
EMAIL_ADDRESS VARCHAR(500),
CREATED_BY VARCHAR(500),
CREATED_DATE DATE,
UPDATED_BY VARCHAR(500),
UPDATED_DATE DATE,
IS_ACTIVE VARCHAR(1),
PRIMARY KEY(CONTACT_ID) );

