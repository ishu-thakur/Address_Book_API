CREATE TABLE MOBILE (
MOBILE_ID SERIAL NOT NULL,
MOBILE_NUMBER VARCHAR(500),
COUNTRY_CODE VARCHAR(500),
TYPE VARCHAR(500),
CREATED_BY VARCHAR(500),
CREATED_DATE DATE,
UPDATED_BY VARCHAR(500),
UPDATED_DATE DATE,
CONTACT_ID INTEGER,
PRIMARY KEY(MOBILE_ID),
FOREIGN KEY (CONTACT_ID) REFERENCES CUSTOMER(CONTACT_ID) );



